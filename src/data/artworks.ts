export function getArtworkById(id: string): Artwork | undefined {
  return artworks.find((a) => a.id === id);
}

export function getArtworkImages(artwork: Artwork): string[] {
  return Array.from({ length: artwork.imageCount }, (_, i) => {
    const num = artwork.imageCount === 1 ? "" : `_${i + 1}`;
    const filename =
      artwork.imageCount === 1
        ? `${artwork.id}.jpg`
        : `${artwork.id}_${i + 1}.jpg`;
    return `/artwork/${artwork.id}/${filename}`;
  });
}

export function getRelatedArtworks(artwork: Artwork, limit = 4): Artwork[] {
  return artworks
    .filter(
      (a) =>
        a.id !== artwork.id &&
        (a.category === artwork.category ||
          a.tags.some((t) => artwork.tags.includes(t)))
    )
    .slice(0, limit);
}
